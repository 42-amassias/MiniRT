t_hit	*hit_create(
			t_hit *hit,
			const t_ray *ray,
			const t_vector3 *normal,
			t_coord t
			)
{
	hit->from_dir = ray->dir;
	hit->t = t;
	ray_at(ray, &hit->p, t);
	hit->normal = *normal;
	return (hit);
}
